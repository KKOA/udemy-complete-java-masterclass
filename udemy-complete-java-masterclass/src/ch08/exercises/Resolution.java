package ch08.exercises;

public class Resolution {

	// fields
	private int width;
	private int height;

	// Constructor
	public Resolution(int width, int height) {
		this.width = width;
		this.height = height;
	}

	// Getters
	public int getWidth() {
		return this.width;
	}

	public int getHeight() {
		return this.height;
	}
}